-- =================================================================================
-- פרויקט בסיס נתונים - שלב ב': טרנזקציות (COMMIT & ROLLBACK)
-- =================================================================================

-- ---------------------------------------------------------
-- דרישה 8: טעות הרסנית וביצוע ROLLBACK (חזרה לאחור)
-- ---------------------------------------------------------
BEGIN; -- פתיחת הבועה (טרנזקציה)

-- עדכון שגוי לחלוטין: מכפילים את כל המחירים בקטלוג ב-100!
UPDATE PRODUCT SET UnitPrice = UnitPrice * 100;

-- בדיקה: הצגת המצב ההרוס (אפשר לראות שהמחירים קפצו לשמיים)
SELECT ProductID, ProductName, UnitPrice FROM PRODUCT LIMIT 5;

-- הצלה: מתחרטים ומבטלים את הפעולה
ROLLBACK;

-- בדיקה: הצגת המצב שחזר לקדמותו (המחירים חזרו להיות רגילים)
SELECT ProductID, ProductName, UnitPrice FROM PRODUCT LIMIT 5;


-- ---------------------------------------------------------
-- דרישה 9: עדכון תקין וביצוע COMMIT (אישור סופי)
-- ---------------------------------------------------------
BEGIN; -- פתיחת טרנזקציה חדשה

-- עדכון תקין: משנים את הסטטוס של הזמנה מספר 1 ל-'Delivered'
UPDATE PURCHASEORDER SET Status = 'Delivered' WHERE OrderID = 1;

-- בדיקה: מראים שהסטטוס אכן השתנה (בתוך הטרנזקציה)
SELECT OrderID, Status FROM PURCHASEORDER WHERE OrderID = 1;

-- אישור: שומרים את הנתונים לתמיד
COMMIT;

-- בדיקה: מראים שהמצב נשמר גם אחרי האישור
SELECT OrderID, Status FROM PURCHASEORDER WHERE OrderID = 1;